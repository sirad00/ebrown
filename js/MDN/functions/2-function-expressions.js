function map(f, a) {
    var result = [], i; // Create a new Array
    for (i = 0; i != a.length; i++)
        result[i] = f(a[i]);
    return result;
}
var f = function (x) {
    return x * x * x;
}
var numbers = [0, 1, 2, 5, 10];
// console.log(f(4));
var cube = map(f, numbers);
